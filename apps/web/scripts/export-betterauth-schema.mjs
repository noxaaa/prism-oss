import { randomUUID } from "node:crypto";
import { Pool } from "pg";
import { getMigrations } from "better-auth/db/migration";

const databaseURL = process.env.TEST_DATABASE_URL ?? process.env.DATABASE_URL;
if (!databaseURL) {
  console.error("TEST_DATABASE_URL or DATABASE_URL is required");
  process.exit(1);
}

const schema = `betterauth_export_${randomUUID().replaceAll("-", "_")}`;

function quoteIdentifier(value) {
  return `"${value.replaceAll('"', '""')}"`;
}

function buildAuthOptions(pool) {
  return {
    database: pool,
    secret: "better-auth-schema-export-secret",
    baseURL: "http://localhost:3000",
    emailAndPassword: {
      enabled: true,
    },
  };
}

const admin = new Pool({ connectionString: databaseURL });
const pool = new Pool({
  connectionString: databaseURL,
  options: `-c search_path=${schema},public`,
});

try {
  await admin.query(`CREATE SCHEMA ${quoteIdentifier(schema)}`);
  const migrations = await getMigrations(buildAuthOptions(pool));
  const sql = await migrations.compileMigrations();
  process.stdout.write(`-- +goose Up
-- Generated by apps/web/scripts/export-betterauth-schema.mjs from better-auth@1.6.16.
-- Do not edit manually; update by regenerating from BetterAuth's official migration API.
${sql}

-- +goose Down
DROP TABLE IF EXISTS "verification";
DROP TABLE IF EXISTS "account";
DROP TABLE IF EXISTS "session";
DROP TABLE IF EXISTS "user";
`);
} finally {
  await pool.end();
  try {
    await admin.query(`DROP SCHEMA IF EXISTS ${quoteIdentifier(schema)} CASCADE`);
  } finally {
    await admin.end();
  }
}
